/****************************************
* Подключение препроцессора и библиотек *
****************************************/
#include <cmath>
#include <iostream>
#include <iomanip>
using namespace std;

/*************
* Переменные *
*************/
float a; // Начальная точка отрезка
float b; // Конечная точка отрезка
float h; // Величина шага
float x; // Аргумент функций
int n; // Количество интервалов
int i = 0; // Счётчик итераций цикла

/****************
* Основное тело *
****************/
int main() {
  // Ввод начальной информации
  cout << "Введите точку А: ";
  cin >> a;
  cout << "Введите точку B: ";
  cin >> b;

  if (a > b){
    cout << "Значение т. A должно быть меньше значения т. B." << endl << "Повторите корректный ввод перезапустив программу." << endl << "Досрочное завершение: Ошибка ввода." << endl;
    exit(1);
  }

  cout << "Введите количество интервалов N: ";
  cin >> n;

  if (n <= 0) {
    cout << "Количество интервалов(N) должно быть больше 0." << endl << "Повторите корректный ввод перезапустив программу." << endl << "Досрочное завершение: Ошибка ввода." << endl;
    exit(1);
  }
  else {
    h = ((b - a) / n); // Вычисление шага
  }

  // Вывод таблицы
  cout << "-----------------------------------------" << endl;
  cout << "| N " 
       << setw(9) << " | X  "
       << setw(13) << " | F     "
       << setw(13) << "  | G         |"
       << endl;
  cout << "-----------------------------------------" << endl;

  // Основные вычисления
  while (i != n) {
    x = (a + (i * h)); // Приращение аргумента
    cout.width(0);
    cout << fixed << setprecision(5) // Установка точности в 5 знаков после запятой
         << "| " << setw(6) 
         << left << i 
         << "| " << setw(8)
         << left << x 
         << "| " << setw(8)
         << left << (2 - cos(x)) 
         << "| " << setw(9)
         << left << (1 - (x * x)) 
         << " |" 
         << endl;
    cout << "-----------------------------------------" << endl;
    i += 1;
    if (a == b) {
      exit(0);
    }
  }
}
